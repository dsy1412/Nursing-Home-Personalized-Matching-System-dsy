import re

# 示例文本数据
text = '''
合肥乐年逍遥津老人服务中心,"所在地区：
                    安徽 -                     合肥市 -                     庐阳区",机构性质：公建民营,负  责  人：胡雪军,开业时间：2011年8月,占地面积：500,建筑面积：1000,床位数：43张,"收住对象：
                    自理 半自理/半失能 不能自理/失能卧床 特护 认知障碍 病后康复",联  系  人：胡雪军,地        址：合肥市庐阳区逍遥津街道吴山巷20号三中宿舍对面
'''

# 定义正则表达式，用于提取数据
pattern = r'''
(.*?),                          # 机构名称
".*?所在地区：\s*(.*?)",        # 地址，去除额外的空格
机构性质：(.*?),                # 机构性质
负\s*责\s*人：(.*?),            # 负责人
开业时间：(.*?),                # 开业时间
占地面积：(.*?),                # 占地面积
建筑面积：(.*?),                # 建筑面积
床位数：(\d+)张,                # 床位数
".*?收住对象：\s*(.*?)",        # 收住对象
联\s*系\s*人：(.*?),            # 联系人
地\s*址：(.*?)$                 # 地址
'''

# 运行正则表达式匹配
match = re.search(pattern, text, re.VERBOSE | re.DOTALL)

if match:
    data = match.groups()
    # 处理地址中的多余空格
    processed_data = [re.sub(r'\s+', ' ', d).strip() for d in data]
    print("Extracted Data:")
    print("\n".join(processed_data))
else:
    print("No match found.")
